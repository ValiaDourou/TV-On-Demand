
package TVDemand;


public class ESeasonFSeries {
    int nos;
    int noe;
    int ry;

    public ESeasonFSeries(int nos, int noe, int ry) {
        this.nos = nos;
        this.noe = noe;
        this.ry = ry;
    }

    public int getNos() {
        return nos;
    }

    public void setNos(int nos) {
        this.nos = nos;
    }

    public int getNoe() {
        return noe;
    }

    public void setNoe(int noe) {
        this.noe = noe;
    }

    public int getRy() {
        return ry;
    }

    public void setRy(int ry) {
        this.ry = ry;
    }
    
}
